/**
 * Админские промпты — в конце system (максимальный вес для модели)
 * и с явным заголовком высшего приоритета.
 */
function appendAdminExtras(
  base: string,
  opts?: { modelSystemExtra?: string | null; chatExtra?: string | null },
): string {
  const modelAdd = (opts?.modelSystemExtra || '').trim().slice(0, 8000);
  const chatAdd = (opts?.chatExtra || '').trim().slice(0, 2000);
  if (!modelAdd && !chatAdd) return base;

  let out = `${base}\n\n---\n${ADMIN_PRIORITY_HEADER}`;
  if (modelAdd) {
    out = `${out}\n\n### Админ-промпт модели (все чаты этой модели)\n${modelAdd}`;
  }
  if (chatAdd) {
    out = `${out}\n\n### Админ-промпт этого чата (ещё выше, чем промпт модели при конфликте)\n${chatAdd}`;
  }
  return out;
}

export function buildSystemPrompt(
  modelId: UiModelId | string,
  extra?: string | null,
  opts?: {
    reasoningPhase?: ReasoningPhase;
    modelSystemExtra?: string | null;
    codingTools?: boolean;
    codingExtra?: string | null;
    webTools?: boolean;
    webExtra?: string | null;
  },
): string {
  const id = normalizeModelId(modelId);
  const modelSystemExtra = opts?.modelSystemExtra ?? null;

  const clock = buildNuclearClockBlock();

  if (opts?.reasoningPhase === 'think') {
    // на think — админ тоже в конце, но без tools; часы всё равно нужны
    return appendAdminExtras(`${thinkOnlySystemPrompt(id)}\n\n---\n${clock}`, {
      modelSystemExtra,
      chatExtra: extra,
    });
  }

  let base = identityPrompt(id);
  if (opts?.reasoningPhase === 'answer') {
    base = `${base}\n\n---\n${REASONING_ANSWER_PROMPT}`;
  }
  // ядерный таймер — до tools, чтобы модель опиралась на актуальное «сейчас»
  base = `${base}\n\n---\n${clock}`;
  if (opts?.webTools && opts.webExtra) {
    base = `${base}\n\n---\n${opts.webExtra}`;
  }
  if (opts?.codingTools && opts.codingExtra) {
    base = `${base}\n\n---\n${opts.codingExtra}`;
  }
  // Админ — всегда последним → самый высокий приоритет поведения
  return appendAdminExtras(base, { modelSystemExtra, chatExtra: extra });
}

